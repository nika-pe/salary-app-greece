def calculate_salary(brutto, category):
    """
    Рассчитывает чистую зарплату, отчисления IKA и налог на основе брутто-зарплаты
    и категории работника в Греции.

    Аргументы:
    brutto (float): Валовая (брутто) зарплата.
    category (str): Категория работника ("misthotos", "eleftheros", "dimosios").

    Возвращает:
    tuple: (net, ika, tax) - чистая зарплата, отчисления IKA и налог.
    """

    # Ставки IKA (страховые взносы) по категориям
    # misthotos: Μισθωτός (Наемный работник)
    # eleftheros: Ελεύθερος επαγγελματίας (Самозанятый/фрилансер)
    # dimosios: Δημόσιος υπάλληλος (Государственный служащий)
    ika_rates = {
        "misthotos": 0.16,
        "eleftheros": 0.26,
        "dimosios": 0.14
    }

    # Получаем ставку IKA для выбранной категории.
    # Если категория не найдена, используется ставка по умолчанию 0.16 (для наемного работника).
    ika_rate = ika_rates.get(category, 0.16)

    # Расчет отчислений IKA
    ika = brutto * ika_rate

    # Доход после вычета IKA
    income_after_ika = brutto - ika

    # Расчет налога на основе дохода после IKA
    # Налоговые ставки (примерные, для демонстрации)
    if income_after_ika <= 10000:
        tax = income_after_ika * 0.09
    else:
        # Налог на первые 10000 евро по ставке 9%, остальное по 22%
        tax = 10000 * 0.09 + (income_after_ika - 10000) * 0.22

    # Расчет чистой зарплаты
    net = brutto - ika - tax

    return net, ika, tax
